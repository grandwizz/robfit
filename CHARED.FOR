        SUBROUTINE CHARED(IOUT)
        INTEGER*2 REGS(10),DL,IOUT,INT,IB
        CHARACTER*2 DISP
        DATA INT/Z21/,DL/ZFF/,IB/Z20/
        IOUT=IB
        REGS(1)=256*6
        REGS(4)=DL
        CALL INTR(INT,REGS)
        LB=REGS(1)
        LB=LB-256*(LB/256)
        IF(LB.EQ.0)RETURN
        IOUT=LB+IB*256
        RETURN
        END
        SUBROUTINE INTR(INT,REGS)
C *** ON THE PC THIS RETURNS IN REG(1) A KEY THAT HAS BEEN PRESSED
C *** ON THE MAINFRAME THIS IS JUST A DUMMY
        INTEGER*2 INT,REGS(10)
        REGS(1)=0
        RETURN
        END
